-- == TIPO DE DATOS ==
char --almacena tipo de datos alfanumericos de ancho fijo.
varchar --alamacena tipo de datos alfanumericos de ancho variable.
text --almacena tipo de datos texto.
nchar --almacena igual que "char", pero si quedan espacios vacios los elimina.
nvarchar --alamacena igual "varchar", pero si quedan espacios vacios los elimina.
bit --alamacena valores de 1 y 0.
int --almacena valores entre -2,147,483,648 y 2,147,483,467.
bigint --almaneca valores entre -9,223,372,036,854,775,808 y 9,223,372,036,854,775,807.
decimal --almacena valores entre -10^38 +1 a 10^38 - 1.
numeric --alamacena igual que "decimal" + enteros.
money --alamcena igual que "bigint" pero especialmente con datos financieros.
float --almacena valores entre -1,79E(EXADECINAL) + 308 a 1.79E(EXADECIMAL) + 308. 